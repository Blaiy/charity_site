<script>
function loadFields() {
    var table = document.getElementById('table').value;
    var fieldsDiv = document.getElementById('fields');

    // Clear existing options
    fieldsDiv.innerHTML = '';

    // Define fields for each table
    var fields = {
        'contacts': ['first_name', 'last_name', 'email', 'message', 'id', 'created_at'],
        'donors': ['name', 'email', 'amount', 'frequency', 'payment_method'],
        'volunteers': ['name', 'email', 'phone', 'category', 'county', 'cv_file']
    };

    // Populate the fields div with checkboxes based on the selected table
    if (fields[table]) {
        fields[table].forEach(field => {
            var label = document.createElement('label');
            var checkbox = document.createElement('input');
            checkbox.type = 'checkbox';
            checkbox.name = 'fields[]';
            checkbox.value = field;
            checkbox.style.marginRight = '10px';
            label.appendChild(checkbox);
            label.appendChild(document.createTextNode(field.charAt(0).toUpperCase() + field.slice(1).replace('_', ' ')));
            fieldsDiv.appendChild(label);
            fieldsDiv.appendChild(document.createElement('br'));
        });
    }
}
</script>

